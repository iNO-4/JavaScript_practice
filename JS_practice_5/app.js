// jQueryの基本書式は以下のようになっていて、今までのJSとは、少し異なる記述ルールになっている。
// $( セレクタ ).メソッド ( パラメータ );
// 以下の記述を当てはめて読み解いていく。
// $('body').html('<h1>Hello jQuery!!</h1>');
// 「セレクタ」は、操作対象になる部分。CSSで馴染みのある呼び名。
// 今回のコードでは、「body」がセレクタ。
// 「メソッド」では、今回は「html」がメソッドにあたる。
// メソッドにパラメータを指定すると、その内容に書き換えられる。
// パラメータを指定しない場合には、メソッドが取得される。
// ・セレクタ......body
// ・メソッド......html
// ・パラメータ....'<h1>Hello jQuery!!</h1>'
// このようにjQueryでは、セレクタとメソッド、さらにパラメータを用いて、「どこに対して何を行うか」を記述している。
// 今回のコードは、「bodyタグ内のHTMLを、<h1>Hello jQuery!!</h1>で書き換えなさい」という意味になる。
