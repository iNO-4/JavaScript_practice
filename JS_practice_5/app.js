// jQueryを書くには、HTMLファイル内に直接記述するか、専用のJSファイルに記述する。

$(document).ready(function () {
	$('body').html('<h1>Hello jQuery!!</h1>');
});

// ブラウザ上に、「Hello jQuery!!」の見出し(h1)が表示される。


